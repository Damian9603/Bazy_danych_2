    private void rejestracjaTelefonKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rejestracjaTelefonKeyTyped
        char key=evt.getKeyChar();
        if(!(Character.isDigit(key))||(key==KeyEvent.VK_BACKSPACE)||(key==KeyEvent.VK_DELETE))
        {
            evt.consume();
        }
    }//GEN-LAST:event_rejestracjaTelefonKeyTyped

    private void rejestracjaImie1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rejestracjaImie1KeyTyped
        char key=evt.getKeyChar();
        if(!((key>64&&key<91)||(key>96&&key<123)))
        {
            evt.consume();
        }
    }//GEN-LAST:event_rejestracjaImie1KeyTyped

    private void rejestracjaNazwiskoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rejestracjaNazwiskoKeyTyped
        char key=evt.getKeyChar();
        if(!((key>64&&key<91)||(key>96&&key<123)||key==45))
        {
            evt.consume();
        }
    }//GEN-LAST:event_rejestracjaNazwiskoKeyTyped

